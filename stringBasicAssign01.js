function myHobbies(){
    var myHobby1 = "Playing Cricket";
    var myHobby2 = "Watching Cricket";
    var myHobby3 = "Swimming";
    console.log(`My hobbies are : ${myHobby1},${myHobby2},${myHobby3}`);    
    var charAtMyHobby1 = myHobby1.length;
    console.log(`Characters available in myHobby1 : ${charAtMyHobby1}`);
    var charAtMyHobby2 = myHobby2.length;
    console.log(`Characters available in myHobby2 : ${charAtMyHobby2}`);
    var charAtMyHobby3 = myHobby3.length;  
    console.log(`Characters available in myHobby3 : ${charAtMyHobby3}`);
    var sum = charAtMyHobby1 + charAtMyHobby2 + charAtMyHobby3;
    console.log(`Sum of total characters in my hobbies : ${sum}`);
    
    
}
myHobbies();

